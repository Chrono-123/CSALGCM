public class Node{
    int val;
    int left;
    int right;

    public void Node(int val){
        this.val = val;
    }

    public void setVal(int val){
        this.val = val;
    }

    public int getVal(){
        return val;
    }

    public void setLeft(int left){
        this.left = left;
    }

    public int getLeft(){
        return left;
    }
    
    public void setRight(int right){
        this.right = right;
    }

    public int getRight(){
        return right;
    }
}
